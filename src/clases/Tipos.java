package clases;


public class Tipos {
    
    public String tipo_genero;
    public String tipo_formato;
    public String tipo_duracion;
    public String tipo_mood;

    public Tipos() { }

    public String getTipo_genero() {
        return tipo_genero;
    }

    public void setTipo_genero(String tipo_genero) {
        this.tipo_genero = tipo_genero;
    }

    public String getTipo_formato() {
        return tipo_formato;
    }

    public void setTipo_formato(String tipo_formato) {
        this.tipo_formato = tipo_formato;
    }

    public String getTipo_duracion() {
        return tipo_duracion;
    }

    public void setTipo_duracion(String tipo_duracion) {
        this.tipo_duracion = tipo_duracion;
    }

    public String getTipo_mood() {
        return tipo_mood;
    }

    public void setTipo_mood(String tipo_mood) {
        this.tipo_mood = tipo_mood;
    }

    
    
    
}
